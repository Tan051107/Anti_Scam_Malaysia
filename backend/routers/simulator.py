@router.post("/chat", response_model=SimulatorChatResponse)
async def simulator_chat(request: SimulatorChatRequest):
    """
    Handle a chat turn in the scam simulation.
    Claude acts as a realistic scammer and determines when the simulation ends.
    """
    session_id = request.session_id or str(uuid.uuid4())
    language = request.language or "en"

    # ── New session: let Claude generate the scenario and opening message ──
    if session_id not in _sessions:
        _sessions[session_id] = {
            "history": [],
            "scam_ended": False,
            "user_caught_scam": False,
            "language": language,
        }
        session = _sessions[session_id]
        system_prompt = build_system_prompt(language)

        opening_messages = [{"role": "user", "content": OPENING_USER_MESSAGE}]
        result = _invoke_simulator(system_prompt, opening_messages)

        session["history"].append({"role": "user", "content": OPENING_USER_MESSAGE})
        session["history"].append({"role": "assistant", "content": result.get("reply", "")})

        scam_ended, user_caught_scam, report = _finalize_turn(session, result)
        return SimulatorChatResponse(
            reply=result.get("reply", ""),
            session_id=session_id,
            scam_ended=scam_ended,
            user_caught_scam=user_caught_scam,
            report=report,
        )

    session = _sessions[session_id]

    # ── Simulation already ended ──
    if session["scam_ended"]:
        return SimulatorChatResponse(
            reply="Simulasi telah tamat. / The simulation has ended. Please reset to start a new one.",
            session_id=session_id,
            scam_ended=True,
            user_caught_scam=session["user_caught_scam"],
            report=session.get("report"),
        )

    # ── Ongoing simulation: append user message and call Claude ──
    history = session["history"]
    new_user_turn = {"role": "user", "content": request.message}
    messages = history + [new_user_turn]

    system_prompt = build_system_prompt(session.get("language", "en"))
    result = _invoke_simulator(system_prompt, messages)

    # Append turns to history only on success
    history.append(new_user_turn)
    history.append({"role": "assistant", "content": result.get("reply", "")})

    # Enforce rolling window
    if len(history) > MAX_TURNS:
        _sessions[session_id]["history"] = history[-MAX_TURNS:]

    scam_ended, user_caught_scam, report = _finalize_turn(session, result)

    return SimulatorChatResponse(
        reply=result.get("reply", ""),
        session_id=session_id,
        scam_ended=scam_ended,
        user_caught_scam=user_caught_scam,
        report=report,
    )


