SIMULATOR_MODEL_ID = "us.anthropic.claude-haiku-4-5-20251001-v1:0"
MAX_TURNS = 20

# ─────────────────────────────────────────────
# In-memory session store
# { session_id: { "history": [...], "scam_ended": bool, "user_caught_scam": bool, "language": str, "report": ScamReport? } }
# ─────────────────────────────────────────────
_sessions: Dict[str, Dict[str, Any]] = {}

OPENING_USER_MESSAGE = (
    "Start the simulation. Invent a new scam scenario and send your opening message as the scammer."
)

# ─────────────────────────────────────────────
# System prompt — scenario fully generated by Claude
# ─────────────────────────────────────────────

